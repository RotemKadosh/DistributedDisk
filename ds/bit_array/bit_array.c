size_t BitsArrCountOn(bit_array_t arr)
{
	size_t count = 0;
	while(0 != arr)
	{
		arr &= (arr - 1);
		++count;
	}
	return count;
}
size_t BitsArrCountOff(bit_array_t arr)
{

	return (sizeof(bit_array_t) * CHAR_BIT) - BitsArrCountOn(arr);
}
