{
	
	return ( arr |= ((bit_array_t)1 << idx));
}
bit_array_t BitsArrSetOff(bit_array_t arr, size_t idx)
{
	bit_array_t mask = (bit_array_t)(-1);
	mask ^= ((bit_array_t)1 << idx);
	return (arr &= mask);
}
