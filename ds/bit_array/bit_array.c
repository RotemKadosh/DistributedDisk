char *BitsArrToString(bit_array_t arr, char *dest)
{
    size_t i = 0;
    char *dest_stert = NULL;
    int digit = 0;
    
    assert(NULL != dest);
    dest_stert = dest;
    dest = dest + (sizeof(bit_array_t) * CHAR_BIT);
    *dest = '\0';
    --dest;

    for (i = 0; i < (sizeof(bit_array_t) * CHAR_BIT); ++i)
    {
        digit = arr & 1;
        *dest = (unsigned char)digit + '0';
        arr >>= 1;
        --dest;
    }

    return dest_stert;
}
